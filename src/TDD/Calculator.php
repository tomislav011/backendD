<?php

namespace TDD;

class Calculator{
    
    public function zbroji($a,$b){
        return $a+$b;
    }

    public function oduzmi($a,$b){
        return $a-$b;
    }

    public function mnozi($a,$b){
        return $a*$b;
    }

    public function dijeli($a,$b){
        return $a/$b;
    }


}

?>